const FONT_LEN: u8 = 65;

#[inline(always)]
pub fn get_char(c: char) -> Option<&'static [u8]> {
    if c.is_ascii() {
        if (c as u8) >= ' ' as u8 {
            let font_offset = (c as u8) - ' ' as u8;

            if font_offset <= FONT_LEN {
                let bitmap_offset = (font_offset as usize) * 8;
                Some(&FONT[bitmap_offset..(bitmap_offset + 8)])
            } else {
                None
            }
        } else {
            None
        }
    } else {
        None
    }
}
